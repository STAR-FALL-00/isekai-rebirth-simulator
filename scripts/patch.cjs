// Helper to insert before a marker
function insertBefore(marker, newCode) {
  const idx = content.indexOf(marker);
  if (idx === -1) {
    console.error('ERROR: Marker not found:', marker.slice(0, 50));
    process.exit(1);
  }
  content = content.slice(0, idx) + newCode + '\n' + content.slice(idx);
}

// Helper to replace a block
function replaceBlock(oldBlock, newBlock) {
  if (!content.includes(oldBlock)) {
    console.error('ERROR: Block not found:', oldBlock.slice(0, 50));
    process.exit(1);
  }
  content = content.replace(oldBlock, newBlock);
}

